def mp_to_value(s: str):
    """'00' -> null, それ以外 -> int"""
    v = int(s)
    return "null" if v == 0 else str(v)


output_lines = []

for raw_line in growth_text.splitlines():
    line = raw_line.strip()
    if not line or line.startswith("Lvl-") or line.startswith("#"):
        continue

    m = re.match(
        r"^\s*(\d+)\s+(\d+)\s+(\d+)\s+(\d+)\s+(\d+)\s+(\d+)\s+.*?\s+"
        r"(\d{2})/(\d{2})/(\d{2})/(\d{2})/(\d{2})/(\d{2})/(\d{2})/(\d{2})",
        raw_line,
    )
    if not m:
        continue

    level = int(m.group(1))
    str_ = m.group(2)
    agi = m.group(3)
    vit = m.group(4)
    int_ = m.group(5)
    mnd = m.group(6)

    mps = [mp_to_value(m.group(i)) for i in range(7, 15)]  # M1～M8

    block = [
        "        {",
        f'          "Level": {level},',
        f'          "Str": {str_},',
        f'          "Agi": {agi},',
        f'          "Vit": {vit},',
        f'          "Int": {int_},',
        f'          "Mnd": {mnd}',
        #        f'          "L1MP": {mps[0]},',
        #        f'          "L2MP": {mps[1]},',
        #        f'          "L3MP": {mps[2]}',
        #        f'          "L4MP": {mps[3]}',
        #        f'          "L5MP": {mps[4]},',
        #        f'          "L6MP": {mps[5]},',
        #        f'          "L7MP": {mps[6]}',
        #        f'          "L8MP": {mps[7]}',
        "        },",
    ]

    output_lines.extend(block)

# JSONファイルとして保存
with open("stats_by_level.json", "w", encoding="utf-8") as f:
    f.write("\n".join(output_lines))

